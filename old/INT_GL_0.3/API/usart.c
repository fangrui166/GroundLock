void UartSend(USART_TypeDef* USARTx,u16 Length,u8* Data)
{
	u16 i = 0;
	if(Length == 0)
		return;
	if(USARTx == USART2)
	{
		CTR485 = 1;
	}
	for(i = 0;i < Length;i ++)
	{
		while(USART_GetFlagStatus(USARTx, USART_FLAG_TC) == RESET);
		USART_SendData(USARTx,Data[i]);	
		//delay_ms(1);
	}
	while(USART_GetFlagStatus(USARTx, USART_FLAG_TC) == RESET);
	if(USARTx == USART2)
	{
		CTR485 = 0;	
	}
}


