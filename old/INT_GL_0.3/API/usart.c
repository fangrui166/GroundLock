void uart_init(u32 bound)
{
	//UART 1
  //GPIO端口设置
  GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;
	 
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_USART1|RCC_APB2Periph_GPIOA, ENABLE);	//使能USART1，GPIOA时钟
  
	//USART1_TX   GPIOA.9
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_9; //PA.9
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;	//复用推挽输出 
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.9 
   
  //USART1_RX	  GPIOA.10初始化 
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10;//PA10 
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;//浮空输入 
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.10   

  //Usart1 NVIC 配置 
  NVIC_InitStructure.NVIC_IRQChannel = USART1_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 3 ;//抢占优先级3 
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 3;		//子优先级3 
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能 
	NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器 
  
   //USART 初始化设置  

	USART_InitStructure.USART_BaudRate = bound;//串口波特率  
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式  
	USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位  
	USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位  
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制 
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式 

  USART_Init(USART1, &USART_InitStructure); //初始化串口1 
  USART_Cmd(USART1, ENABLE);                    //使能串口1  
  USART_ITConfig(USART1, USART_IT_IDLE, ENABLE);//打开空闲中断
  USART_ITConfig(USART1, USART_IT_RXNE, ENABLE);//开启串口接受中断 
  USART_ITConfig(USART1, USART_IT_PE, ENABLE);  //打开奇偶错误中断
  USART_ITConfig(USART1, USART_IT_ERR, ENABLE); //打开错误中断 
	//UART 2
  //GPIO端口设置

	RCC_APB1PeriphClockCmd(RCC_APB1Periph_USART2, ENABLE);	//使能USART1，GPIOA时钟
  
	//USART1_TX   GPIOA.2
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2; //PA2
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;	//复用推挽输出 
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA2 
   
  //USART1_RX	  GPIOA.3初始化 
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_3;//PA3
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;//浮空输入 
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA3   

  //Usart2 NVIC 配置 
  NVIC_InitStructure.NVIC_IRQChannel = USART2_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 3 ;//抢占优先级3 
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 3;		//子优先级3 
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能 
	NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器 
  
   //USART 初始化设置  

	USART_InitStructure.USART_BaudRate = 9600;//串口波特率  
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式  
	USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位  
	USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位  
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制 
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式 

  USART_Init(USART2, &USART_InitStructure); //初始化串口1 
  USART_Cmd(USART2, ENABLE);                    //使能串口1  
  USART_ITConfig(USART2, USART_IT_IDLE, ENABLE);//打开空闲中断
  USART_ITConfig(USART2, USART_IT_RXNE, ENABLE);//开启串口接受中断 
  USART_ITConfig(USART2, USART_IT_PE, ENABLE);  //打开奇偶错误中断
  USART_ITConfig(USART2, USART_IT_ERR, ENABLE); //打开错误中断 
	//UART 3
  //GPIO端口设置
	//RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE);	
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_USART3, ENABLE);	
	//USART3_TX   GPIOB.10
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10; 
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;	//复用推挽输出 
  GPIO_Init(GPIOB, &GPIO_InitStructure);
  //USART3_RX	  GPIOB.11初始化 
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_11;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;//浮空输入 
  GPIO_Init(GPIOB, &GPIO_InitStructure);
  //Usart3 NVIC 配置 
  NVIC_InitStructure.NVIC_IRQChannel = USART3_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority=3 ;//抢占优先级3 
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 2;		//子优先级3 
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能 
	NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器 
   //USART 初始化设置  
	USART_InitStructure.USART_BaudRate = 9600;//串口波特率  
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式  
	USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位  
	USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位  
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制 
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式 

  USART_Init(USART3, &USART_InitStructure); //初始化串口3 
  USART_Cmd(USART3, ENABLE);                    //使能串口1  
  USART_ITConfig(USART3, USART_IT_IDLE, ENABLE);//打开空闲中断
  USART_ITConfig(USART3, USART_IT_RXNE, ENABLE);//开启串口接受中断 
  USART_ITConfig(USART3, USART_IT_PE, ENABLE);  //打开奇偶错误中断
  USART_ITConfig(USART3, USART_IT_ERR, ENABLE); //打开错误中断 
}

